/**
 * Directory Page Search, URL Routing Parameters Interception and Filtering Logic
 */
function initDirectoryEngine() {
    const streamContainer = document.getElementById('directoryStreamContainer');
    const searchInput = document.getElementById('directorySearchInput');
    const filterForm = document.getElementById('sidebarFilterForm');
    const resultsCountText = document.getElementById('directoryResultsCount');
    const emptyState = document.getElementById('directoryEmptyState');

    if (!streamContainer || !searchInput || !filterForm) return;

    // Scan URL query tokens to extract initial entry parameters forwarded from Home Hero quick search
    const urlParams = new URLSearchParams(window.location.search);
    const initialSearch = urlParams.get('search') || '';
    const initialDept = urlParams.get('department') || 'all';

    // Populate interface elements to correspond with incoming parameter state tokens
    searchInput.value = initialSearch;
    const targetRadio = filterForm.querySelector(`input[value="${initialDept}"]`);
    if (targetRadio) targetRadio.checked = true;

    // Master filter pipeline logic execution node
    const executeFilterPipeline = () => {
        const searchQuery = searchInput.value.toLowerCase().trim();
        const activeDeptRadio = filterForm.querySelector('input[name="deptFilter"]:checked');
        const selectedDept = activeDeptRadio ? activeDeptRadio.value : 'all';

        const filteredSet = window.careConnectDoctorsData.filter(doc => {
            const matchesDept = (selectedDept === 'all' || doc.department === selectedDept);
            const matchesSearch = (
                doc.name.toLowerCase().includes(searchQuery) ||
                doc.specialization.toLowerCase().includes(searchQuery) ||
                doc.hospital.toLowerCase().includes(searchQuery) ||
                doc.department.toLowerCase().includes(searchQuery)
            );
            return matchesDept && matchesSearch;
        });

        // Update view nodes based on pipeline evaluations
        resultsCountText.textContent = `Showing ${filteredSet.length} board-certified medical professionals available for dispatch.`;
        
        if (filteredSet.length === 0) {
            streamContainer.innerHTML = '';
            emptyState.style.display = 'block';
            return;
        }

        emptyState.style.display = 'none';
        streamContainer.innerHTML = '';

        filteredSet.forEach(doc => {
            const card = document.createElement('div');
            card.className = 'doctor-card-ui';
            card.innerHTML = `
                <div class="doc-card-image-wrapper">
                    <span class="doc-image-placeholder">${doc.photo}</span>
                    <div class="doc-card-rating-badge"><span>★</span> ${doc.rating.toFixed(1)}</div>
                </div>
                <div class="doc-card-body">
                    <span class="doc-card-dept">${doc.department}</span>
                    <h3>${doc.name}</h3>
                    <p class="doc-meta-string">${doc.specialization}</p>
                    <div class="doc-data-row"><span>Hospital:</span><strong>${doc.hospital.split(',')[0]}</strong></div>
                    <div class="doc-data-row"><span>Exp:</span><strong>${doc.experience} Yrs</strong></div>
                    <div class="doc-data-row"><span>Fee:</span><strong>₹${doc.consultationFee}</strong></div>
                </div>
                <div class="doc-card-footer">
                    <a href="doctor-details.html?id=${doc.id}" class="btn btn-outline btn-full">Inspect Schedule</a>
                </div>
            `;
            streamContainer.appendChild(card);
        });
    };

    // Bind real-time input and change observation nodes
    searchInput.addEventListener('input', executeFilterPipeline);
    filterForm.addEventListener('change', executeFilterPipeline);

    // Initial load sync pass
    executeFilterPipeline();
}